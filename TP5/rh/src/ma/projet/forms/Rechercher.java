    public void loadProfil(){
         for(Profil p : pf.getAll()){
             listeProfil.addItem(p);
         }
    }
    public void loadFindProfil(){
        m.setRowCount(0);
        
          for (Personne p: per.findByProfil((Profil)listeProfil.getSelectedItem())){
              m.addRow(new Object[]{
              p.getId(),p.getNom(),p.getPrenom(),p.getDateNaiss()   
              });
          }
       
    }

