    public void loadService(){
         for(Service s : ss.findAll()){
             listeService.addItem(s);
         }
    }
    public void loadFindService(){
        m.setRowCount(0);
        
          for (Client c: cs.findByService((Service)listeService.getSelectedItem())){
              m.addRow(new Object[]{
               c.getId(),c.getNom(),c.getPrenom(),c.getDate(),c.getService()    
              });
          }
       
    }

